package com.withice.dto;

public class CategoryAndCnt {

	private int no;
	private String name;
	private int mainCateNo;
	private int count;
	
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getMainCateNo() {
		return mainCateNo;
	}
	public void setMainCateNo(int mainCateNo) {
		this.mainCateNo = mainCateNo;
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
}
